//Example of operator : ++

#include<stdio.h> //required header file

int main()//prg starts here
{
    int a;
    a = 10;
    printf("\n before increment a : %d ", a);
    //a++;
    ++a;
    printf("\n after increment a : %d ", a);
    
    return 0; //end of prg
}//main

